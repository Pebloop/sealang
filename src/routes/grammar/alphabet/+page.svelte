			<h3 class="text-2xl">Where does they come from ?</h3>
			<hr/>

			<p>Sea dragons talk by illuminating parts of their body. Each patterns represent a character and
			multiple patterns together form words.</p>
			<div class="flex flex-row items-center justify-center">
				<img src="/grammar/sea_dragon_patterns.png" alt="sea dragon patterns" class="w-1/2 h-auto mt-4 mb-4" />
			</div>


			<p>With the invention of writing, patterns got simplified to fit the disks the sea dragons use.</p>

			<div class="flex flex-row items-center justify-center">
				<img src="/grammar/writing_disk.png" alt="writing disk" class="w-1/2 h-auto mt-4 mb-4" />
			</div>

			<p>Each pattern is represented by a combination of 6 lights. Each light can be on or off.
				The top one represent the head, the middle one the body, the left and right the arms and the two at the bottom the legs.
			  The tail, which represented the intensity, or expression in the language got lost in the process.
			It is still used when 'talking' but in writing you have to use "intention" words.</p>

			<p>Here are some examples of characters and what part of the body they represent:</p>
			<div class="flex flex-row items-center justify-center">
				<img src="/grammar/characters.png" alt="characters" class="w-1/2 h-auto mt-4 mb-4" />
			</div>

			<p>
				One more thing to remember about characters that might help to better remember words is that each light
				has a key meaning :
			</p>
			<div class="flex flex-row items-center justify-center">
				<ul class="list-disc m-4">
					<li >Head : inaction</li>
					<li>Body : action</li>
					<li>Left arm : mind</li>
					<li>Right arm : present</li>
					<li>Left leg : mater</li>
					<li>Right leg : past</li>
				</ul>
			</div>

			<p>
				For example, the character <span class="font-sealang">d</span> is made of the head and the left arm on.
				It mean that it's a verb of inaction of the mind. Similary, <span class="font-sealang">e</span>, "to have",
				is a verb of inaction of mater. Sometime the  lights used are not related to its core meaning,
				like <span class="font-sealang">f</span>, "to eat", which is a verb of action of the head, because you eat with your head.
			</p>

			<p>
				Finally the character <span class="font-sealang">?</span> is a special character. It is used to separate words in a sentence.
				You will never see it in a word.
			</p>
